# Escreva um algoritmo para ler as dimensões de um retângulo
# (base e altura), calcular e escrever a área do retângulo.

base = int(input('Digite a medida da base do retângulo em cm: '))
altura = int(input('Digite a medida da altura do retângulo em cm: '))
area = base*altura

print('A área do retângulo é {}cm²'.format(area))

